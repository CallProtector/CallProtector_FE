const Chatbot = () => {
  const [activeTab, setActiveTab] = useState('ÏùºÎ∞ò');
  const [showModal, setShowModal] = useState(false);

  // ÏùºÎ∞ò ÏÑ∏ÏÖò: [{sessionId, title, startTime}]
  const [generalChatSessions, setGeneralChatSessions] = useState([]);
  const [consultChatSessions, setConsultChatSessions] = useState([]);
  const [generalChatMap, setGeneralChatMap] = useState({});
  const [consultChatMap, setConsultChatMap] = useState({});
  const [selected, setSelected] = useState(null);
  const [inputText, setInputText] = useState('');
  const [tempSessionId, setTempSessionId] = useState(null);

  const currentChatMap = activeTab === 'ÏùºÎ∞ò' ? generalChatMap : consultChatMap;
  const setCurrentChatMap = activeTab === 'ÏùºÎ∞ò' ? setGeneralChatMap : setConsultChatMap;
  const messages = selected ? currentChatMap[selected] || [] : [];

  const selectedSessionMeta =
    activeTab === 'ÏùºÎ∞ò'
      ? generalChatSessions.find(s => String(s.sessionId) === String(selected))
      : null;

  // Bot Î©îÏãúÏßÄ Ìè¨Îß∑ÌÑ∞ (answer + sourcePages)
  const formatBotMessage = (answer, sourcePages) => {
    let formatted = `${answer || ''}`;
    if (Array.isArray(sourcePages) && sourcePages.length > 0) {
      formatted += '\n\n üë©‚öñÔ∏èÎ≤ïÏ†ÅÏúºÎ°ú Ïù¥Î†áÍ≤å ÎåÄÏùëÌï† Ïàò ÏûàÏñ¥Ïöî! \n';
      formatted += sourcePages
        .map(sp => `‚Ä¢ Ïú†Ìòï: ${sp?.Ïú†Ìòï}\n‚Ä¢ Í¥ÄÎ†®Î≤ïÎ•†: ${sp?.Í¥ÄÎ†®Î≤ïÎ•† || 'ÏóÜÏùå'}`)
        .join('\n');
    }
    return formatted.trim();
  };

  // ÏùºÎ∞ò ÏÑ∏ÏÖò Î™©Î°ù Î°úÎìú (ÏûêÎèô ÏÑ†ÌÉù Ï†úÍ±∞)
  const loadGeneralSessions = async () => {
    try {
      const res = await fetch('http://localhost:8080/api/chat-session/list', {
        method: 'GET',
        headers: { Authorization: `Bearer ${localStorage.getItem('accessToken')}` },
      });
      const data = await res.json();

      if (res.ok && data.isSuccess && Array.isArray(data.result)) {
        const list = [...data.result].sort(
          (a, b) => new Date(b.startTime).getTime() - new Date(a.startTime).getTime()
        );

        setGeneralChatSessions(list);

        setGeneralChatMap(prev => {
          const next = { ...prev };
          list.forEach(({ sessionId }) => {
            if (!next[sessionId]) next[sessionId] = [];
          });
          return next;
        });

      } else {
        console.warn('ÏÑ∏ÏÖò Î™©Î°ù Ï°∞Ìöå Ïã§Ìå®:', data?.message);
      }
    } catch (e) {
      console.error('ÏÑ∏ÏÖò Î™©Î°ù Ìò∏Ï∂ú Ïò§Î•ò:', e);
    }
  };

  // ÏÑ†ÌÉùÎêú ÏÑ∏ÏÖòÏùò Î°úÍ∑∏ Î°úÎìú
  const loadChatLogs = async (sessionId) => {
    try {
      const res = await fetch(`http://localhost:8080/api/chat-log/session/${sessionId}`, {
        method: 'GET',
        headers: { Authorization: `Bearer ${localStorage.getItem('accessToken')}` },
      });
      const data = await res.json();

      if (res.ok && data.isSuccess && Array.isArray(data.result)) {
        const logs = [...data.result].sort(
          (a, b) => new Date(a.createdAt).getTime() - new Date(b.createdAt).getTime()
        );

        const msgList = [];
        logs.forEach(item => {
          if (item.question) {
            msgList.push({ fromUser: true, text: item.question });
          }
          msgList.push({
            fromUser: false,
            text: formatBotMessage(item.answer, item.sourcePages),
          });
        });

        setGeneralChatMap(prev => ({ ...prev, [sessionId]: msgList }));
      } else {
        console.warn('ÎåÄÌôî Î°úÍ∑∏ Ï°∞Ìöå Ïã§Ìå®:', data?.message);
      }
    } catch (e) {
      console.error('ÎåÄÌôî Î°úÍ∑∏ Ìò∏Ï∂ú Ïò§Î•ò:', e);
    }
  };

  // ‚úÖ ÏÉà ÏÑ∏ÏÖò ÏÉùÏÑ± Î≥¥Ïû• (ÏÑ†ÌÉù ÏóÜÏùÑ Îïå Ìò∏Ï∂ú)
  const ensureSessionId = async () => {
    if (selected && /^\d+$/.test(String(selected))) return selected;

    const res = await fetch('http://localhost:8080/api/chat-session', {
      method: 'POST',
      headers: {
        Authorization: `Bearer ${localStorage.getItem('accessToken')}`,
        'Content-Type': 'application/json',
      },
    });
    const data = await res.json();
    if (!res.ok || !data.isSuccess || !data.result?.sessionId) {
      throw new Error(data?.message || 'ÏÑ∏ÏÖò ÏÉùÏÑ± Ïã§Ìå®');
    }
    const newId = data.result.sessionId;

    // Î™©Î°ù ÏÉÅÎã®Ïóê Ï∂îÍ∞Ä
    setGeneralChatSessions(prev => [
      { sessionId: newId, title: null, startTime: new Date().toISOString() },
      ...prev,
    ]);

    setGeneralChatMap(prev => ({ ...prev, [newId]: [] }));
    setSelected(newId);

    return newId;
  };

  // Ï≤´ ÏßÑÏûÖ
  useEffect(() => {
    loadGeneralSessions();
  }, []);

  // ÌÉ≠ Ï†ÑÌôò
  useEffect(() => {
    if (activeTab === 'ÏùºÎ∞ò') {
      loadGeneralSessions();
    } else {
      setSelected(null);
    }
  }, [activeTab]);

  // ÏÑ∏ÏÖò ÏÑ†ÌÉù Ïãú Ìï¥Îãπ Î°úÍ∑∏ Î°úÎìú(ÏùºÎ∞ò ÌÉ≠Îßå, ÏïÑÏßÅ Î©îÏãúÏßÄ ÏóÜÏúºÎ©¥)
  useEffect(() => {
    if (activeTab !== 'ÏùºÎ∞ò') return;
    if (!selected) return;

    const alreadyLoaded = Array.isArray(generalChatMap[selected]) && generalChatMap[selected].length > 0;
    if (!alreadyLoaded) {
      const isNumeric = String(selected).match(/^\d+$/);
      if (isNumeric) loadChatLogs(selected);
    }
  }, [selected, activeTab, generalChatMap]);

  const startNewChat = async () => {
    try {
      const res = await fetch('http://localhost:8080/api/chat-session', {
        method: 'POST',
        headers: {
          Authorization: `Bearer ${localStorage.getItem('accessToken')}`,
          'Content-Type': 'application/json',
        },
      });
      const data = await res.json();
      if (res.ok && data.isSuccess && data.result?.sessionId) {
        const sessionId = data.result.sessionId;
        setSelected(sessionId);

        setGeneralChatSessions(prev => [
          { sessionId, title: null, startTime: new Date().toISOString() },
          ...prev,
        ]);

        setGeneralChatMap(prev => ({ ...prev, [sessionId]: [] }));
        setInputText('');
      } else {
        alert('ÏÑ∏ÏÖò ÏÉùÏÑ± Ïã§Ìå®: ' + data.message);
      }
    } catch (error) {
      alert('ÏÑúÎ≤Ñ Ïò§Î•òÎ°ú ÏÑ∏ÏÖòÏùÑ ÏÉùÏÑ±Ìï† Ïàò ÏóÜÏäµÎãàÎã§.');
    }
  };

  const handleSend = async () => {
    const text = inputText.trim();
    if (!text) return;

    const sessionId = await ensureSessionId();

    const userMessage = { fromUser: true, text };
    const chatMap = activeTab === 'ÏùºÎ∞ò' ? generalChatMap : consultChatMap;

    if (!chatMap[sessionId]) {
      setCurrentChatMap(prev => ({ ...prev, [sessionId]: [userMessage] }));
    } else {
      setCurrentChatMap(prev => ({
        ...prev,
        [sessionId]: [...prev[sessionId], userMessage],
      }));
    }

    setInputText('');
    setTempSessionId(null);

    try {
      const token = localStorage.getItem('accessToken');
      const encoded = encodeURIComponent(text);
      const url = `http://localhost:8080/api/chat/stream?sessionId=${sessionId}&question=${encoded}&token=${token}`;

      const eventSource = new EventSource(url);
      let buffer = '';

      const appendBotMessage = chunk => {
        setCurrentChatMap(prev => {
          const existing = prev[sessionId] || [];
          const lastMsg = existing[existing.length - 1];
          if (lastMsg && !lastMsg.fromUser) {
            const updated = [...existing];
            updated[updated.length - 1] = {
              ...lastMsg,
              text: lastMsg.text + chunk,
            };
            return { ...prev, [sessionId]: updated };
          } else {
            return {
              ...prev,
              [sessionId]: [...existing, { fromUser: false, text: chunk }],
            };
          }
        });
      };

      eventSource.onmessage = event => {
        const chunk = event.data;

        if (chunk === '[END]') {
          try {
            buffer = buffer.trim();
            const jsonStart = buffer.indexOf('{');
            const jsonEnd = buffer.lastIndexOf('}') + 1;
            const jsonString = buffer.substring(jsonStart, jsonEnd).trim();
            const parsed = JSON.parse(jsonString);

            if (parsed.answer) {
              const formatted = formatBotMessage(parsed.answer, parsed.sourcePages);
              appendBotMessage(formatted);
            }
          } catch (e) {
            console.warn('JSON ÌååÏã± Ïã§Ìå®:', e);
            appendBotMessage('[‚ö†Ô∏è ÏùëÎãµ ÌååÏã± Ïã§Ìå®]');
          }

          eventSource.close();
          return;
        }

        if (chunk.startsWith('[JSON]')) {
          buffer = chunk.replace('[JSON]', '').trim();
        }
      };

      eventSource.onerror = e => {
        console.error('‚õî SSE Ïó∞Í≤∞ Ïò§Î•ò', e);
        appendBotMessage('[‚õî Ïó∞Í≤∞ Ïã§Ìå®]');
        eventSource.close();
      };
    } catch (err) {
      console.error('Ïä§Ìä∏Î¶¨Î∞ç Ï≤òÎ¶¨ Ï§ë Ïò§Î•ò:', err);
      alert('Î©îÏãúÏßÄÎ•º Í∞ÄÏ†∏Ïò§Îäî Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.');
    }
  };

  return (
    <Container>
      <Sidebar>
        <Tabs>
          <Tab active={activeTab === 'ÏùºÎ∞ò'} onClick={() => setActiveTab('ÏùºÎ∞ò')}>
            ÏùºÎ∞ò
          </Tab>
          <Tab active={activeTab === 'ÏÉÅÎã¥Î≥Ñ'} onClick={() => setActiveTab('ÏÉÅÎã¥Î≥Ñ')}>
            ÏÉÅÎã¥Î≥Ñ
          </Tab>
        </Tabs>

        {activeTab === 'ÏùºÎ∞ò' ? (
          <SidebarActionButton onClick={startNewChat}>
            <FaPlus size={14} /> ÏÉàÎ°úÏö¥ Ï±ÑÌåÖ
          </SidebarActionButton>
        ) : (
          <SidebarActionButton onClick={() => setShowModal(true)}>
            <FaPlus size={14} /> ÏÉÅÎã¥ ÎÇ¥Ïó≠ Î∂àÎü¨Ïò§Í∏∞
          </SidebarActionButton>
        )}

        {showModal && <ChatListModal onClose={() => setShowModal(false)} />}

        <ChatList>
          {activeTab === 'ÏùºÎ∞ò'
            ? generalChatSessions.map(({ sessionId, title, startTime }) => (
                <ChatItem
                  key={sessionId}
                  selected={String(selected) === String(sessionId)}
                  onClick={() => setSelected(sessionId)}
                >
                  <div style={{ fontWeight: 600 }}>
                    {title || `ÏÉà ÎåÄÌôî #${sessionId}`}
                  </div>
                  <div style={{ fontSize: 12, color: '#888', marginTop: 2 }}>
                    {new Date(startTime).toLocaleString()}
                  </div>
                </ChatItem>
              ))
            : consultChatSessions.map(sessionId => (
                <ChatItem
                  key={sessionId}
                  selected={String(selected) === String(sessionId)}
                  onClick={() => setSelected(sessionId)}
                >
                  {sessionId}
                </ChatItem>
              ))}
        </ChatList>
      </Sidebar>

      <ChatArea>
        {selected && messages.length > 0 ? (
          <>
            <ChatHeader>
              <ChatTitle>
                {activeTab === 'ÏùºÎ∞ò'
                  ? (selectedSessionMeta?.title || `ÏÉà ÎåÄÌôî #${selected}`)
                  : selected}
                <CallLogButton
                  style={{
                    visibility: activeTab === 'ÏÉÅÎã¥Î≥Ñ' ? 'visible' : 'hidden',
                  }}
                >
                  ÌÜµÌôî ÎÇ¥Ïö© Î≥¥Í∏∞
                </CallLogButton>
              </ChatTitle>
              <ChatDate>
                {activeTab === 'ÏùºÎ∞ò' && selectedSessionMeta?.startTime
                  ? new Date(selectedSessionMeta.startTime).toLocaleString()
                  : ' '}
              </ChatDate>
            </ChatHeader>
            <ChatBody>
              {messages.map((msg, idx) => (
                <ChatBubble key={idx} fromUser={msg.fromUser}>
                  {msg.text}
                </ChatBubble>
              ))}
            </ChatBody>
          </>
        ) : (
          <ChatBody>
            <EmptyMessage>
              ÏÉàÎ°úÏö¥ Ï±ÑÌåÖÏùÑ ÏãúÏûëÌï¥Î≥¥ÏÑ∏Ïöî. 
              <br /> ÏôºÏ™ΩÏùò 'ÏÉàÎ°úÏö¥ Ï±ÑÌåÖ' Î≤ÑÌäºÏùÑ ÎàÑÎ•¥Í±∞ÎÇò
              ÏïÑÎûò ÏûÖÎ†•Ï∞ΩÏóê ÏßàÎ¨∏ÏùÑ ÏûÖÎ†•ÌïòÎ©¥ ÏûêÎèôÏúºÎ°ú ÏÉà ÎåÄÌôîÍ∞Ä ÏÉùÏÑ±Îê©ÎãàÎã§.
            </EmptyMessage>
          </ChatBody>
        )}

        <InputArea>
          <InputWrapper>
            <Input
              placeholder="Î©îÏãúÏßÄÎ•º ÏûÖÎ†•ÌïòÏÑ∏Ïöî"
              value={inputText}
              onChange={e => setInputText(e.target.value)}
              onKeyDown={e => {
                if (e.key === 'Enter') handleSend();
              }}
            />
            <SendButton onClick={handleSend}>
              <FiSend size={24} />
            </SendButton>
          </InputWrapper>
        </InputArea>
      </ChatArea>
    </Container>
  );
};

